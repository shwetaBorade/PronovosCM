package com.pronovoscm.model.response.transferlog;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.List;

public class Data {
    @SerializedName("responseMsg")
    private String responsemsg;
    @SerializedName("responseCode")
    private int responsecode;
    @SerializedName("logs")
    private List<Logs> logs;
    @SerializedName("hasTransfers")
    private boolean hastransfers;

    public String getResponsemsg() {
        return responsemsg;
    }

    public void setResponsemsg(String responsemsg) {
        this.responsemsg = responsemsg;
    }

    public int getResponsecode() {
        return responsecode;
    }

    public void setResponsecode(int responsecode) {
        this.responsecode = responsecode;
    }

    public List<Logs> getLogs() {
        return logs;
    }

    public void setLogs(List<Logs> logs) {
        this.logs = logs;
    }

    public boolean getHastransfers() {
        return hastransfers;
    }

    public void setHastransfers(boolean hastransfers) {
        this.hastransfers = hastransfers;
    }
}
