package com.pronovos.pdf.utils;

public class AnnotAction {
    String action;
    String content;

    public AnnotAction() {
    }

    public String getAction() {
        return this.action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getContent() {
        return this.content;
    }

    public void setContent(String content) {
        this.content = content;
    }

}
